package groupChat;

public class SwitchCase {

	public static void main(String[] args) {
		int name = 7;
		switch (name)
		{
		
		case 1 : System.out.println("jamshed");break;
		case 2 : System.out.println("monir");break;
		case 3 : System.out.println("masud");break;
		case 4 : System.out.println("razu");break;
		case 5 : System.out.println("hasan");break;
		case 6 : System.out.println("faruq");break;
		case 7 : System.out.println("borhan");break;
		default: System.out.println("shadeq");
		
		
		
		
		
		
		}

	}

}
